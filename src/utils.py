def multiply(a: float, b: float) -> float:
    """Multiply a and b.
    
    Args:
        a : first float
        b : second float
    """
    return a * b

def add(a: float, b: float) -> float:
    """Adds a and b.
    
    Args:
        a : first float
        b : second float
    """
    return a + b

def subtract(a: float, b: float) -> float:
    """Subtracts a and b.
    
    Args:
        a : first float
        b : second float
    """
    return a - b

def divide(a: float, b: float) -> float:
    """Divides a and b.
    
    Args:
        a : first float
        b : second float
    """
    return a / b
